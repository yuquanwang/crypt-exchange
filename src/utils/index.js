function deepCopy(dest, src) {
  if (!src) {
    return
  }
  Object.keys(src).forEach((key) => {
    const srcVal = src[key]
    // eslint-disable-next-line no-prototype-builtins
    if (dest.hasOwnProperty(key)) {
      // 属性冲突
      const destVal = dest[key]
      if (srcVal instanceof Object) {
        if (destVal instanceof Object) {
          // 进入下一层,继续拷贝
          deepCopy(destVal, srcVal)
        } else {
          // src的属性被覆盖
          dest[key] = srcVal
          console.warn(`国际化信息: ${destVal} 被覆盖了`)
        }
      } else {
        // 被ignore掉
        console.warn(`国际化信息: ${srcVal} 被忽略了`)
      }
    } else {
      // 直接赋值
      dest[key] = srcVal
    }
  })
}

/**
 * 验证两条数据是否相同
 * @param {Array | Object} data1  [需要进行验证的数据1]
 * @param {Array | Object} data2 [需要进行验证的数据2]
 */
export function isDataSame(data1, data2) {
  if (data1 === data2) {
    return true
  }
  if ((data1 && !data2) || (!data1 && data2)) {
    return false
  }
  let bol = true
  if (Object.keys(data1).length !== Object.keys(data2).length) {
    return false
  }
  for (const key in data1) {
    if (typeof data1[key] === 'object') {
      bol = isDataSame(data1[key], data2[key])
      if (!bol) {
        break
      }
    } else if (data1[key] !== data2[key]) {
      bol = false
      break
    }
  }
  return bol
}

const lodash = require('lodash')

export function genTree(list, key, pkey, root = null, current = {}, tree = []) {
  if (lodash.isEmpty(current)) {
    current[key] = root
  }
  const subList = lodash.filter(list, (item) => {
    return item[pkey] === current[key]
  })
  if (!lodash.isEmpty(subList)) {
    if (current[key] === root) {
      tree = subList
    } else {
      current['children'] = subList
    }

    subList.map((sub) => {
      genTree(list, key, pkey, root, sub, tree)
    })
  }
  return tree
}

const isObject = (obj) => Object.prototype.toString.call(obj) === '[object Object]'
export const filterEmptyField = (obj) => {
  if (isObject(obj)) {
    const temp = {}
    Object.keys(obj).forEach((key) => {
      if (!(obj[key] === null || obj[key] === '' || obj[key] === undefined)) {
        temp[key] = obj[key]
      }
    })
    return temp
  }
  return obj
}

export const filterStringLeadingAndTrailingSpaces = (obj) => {
  if (isObject(obj)) {
    Object.keys(obj).forEach((key) => {
      if (typeof obj[key] === 'string') {
        obj[key] = obj[key].trim()
      }
    })
  }
  return obj
}

export const generateUuid = () => {
  const s = []
  const hexDigits = '0123456789abcdef'
  for (let i = 0; i < 36; i++) {
    s[i] = hexDigits.substr(Math.floor(Math.random() * 0x10), 1)
  }
  s[14] = '4' // bits 12-15 of the time_hi_and_version field to 0010
  s[19] = hexDigits.substr((s[19] & 0x3) | 0x8, 1) // bits 6-7 of the clock_seq_hi_and_reserved to 01
  s[8] = s[13] = s[18] = s[23] = '-'

  return s.join('')
}

